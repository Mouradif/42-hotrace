#include <libft.h>

char	*ft_strsub(char const *s, unsigned int start, size_t len)
{
	char		*res;
	size_t		i;

	i = 0;
	res = (char*)malloc(sizeof(char) * len + 1);
	if (!res)
		return (res);
	while (i < len)
	{
		res[i] = s[start + i];
		i++;
	}
	res[i] = '\0';
	return (res);
}
